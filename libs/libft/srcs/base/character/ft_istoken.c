#include "libft_base/character.h"

int		ft_istoken(int c)
{
	return (
			(c >= 'a' && c <= 'z') ||
			(c >= 'A' && c <= 'Z') ||
			(c >= '0' && c <= '9') ||
			c == '.' || c == '-' || c == '+');
}
